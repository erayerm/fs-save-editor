// scripts/build-weapon-data.mjs
// Generate scripts/lib/weaponData.mjs from the AUTHORITATIVE game data:
//   - GameParameters.prefab : every weapon's m_WeaponId, m_DamageMin/Max,
//       m_NameLocalizationId, m_WeaponSprite
//   - I2Languages.prefab    : localization term -> English display name
//
// Why: the previous weaponData.mjs was hand-curated from community wikis and had
// ~42 wrong ids (e.g. "RelentlessRaiderSword" vs real "Melee_RaiderSword",
// "GragnaksAxe" vs "GrognaksAxe", a phantom "Switchblade") plus wrong damages.
// Assigning a wrong id makes the GAME silently replace it with the default
// weapon (.32 pistol). Reading the ids straight from the game prefab fixes that.
//
// Usage: node scripts/build-weapon-data.mjs
import { readFileSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';

const ROOT = 'TEMPORARY-game-files/export-3/ExportedProject/Assets';
const GAME_PARAMS = join(ROOT, 'GameObject/GameParameters.prefab');
const I2 = join(ROOT, 'Resources/I2Languages.prefab');
const OUT = 'scripts/lib/weaponData.mjs';

// --- Parse localization: term -> English (first Languages entry) ---
function parseLocalization(text) {
  const lines = text.split(/\r?\n/);
  const map = new Map();
  for (let i = 0; i < lines.length; i++) {
    const m = lines[i].match(/^\s*- Term:\s*(.+?)\s*$/);
    if (!m) continue;
    const term = m[1];
    // Find the "Languages:" line for this term, then the first "- <value>".
    for (let j = i + 1; j < lines.length && j < i + 12; j++) {
      if (/^\s*- Term:/.test(lines[j])) break; // next term, no Languages found
      if (/^\s*Languages:\s*$/.test(lines[j])) {
        const v = lines[j + 1].match(/^\s*-\s?(.*)$/);
        if (v) map.set(term, v[1].trim());
        break;
      }
    }
  }
  return map;
}

// --- Parse weapons from GameParameters.prefab ---
function parseWeapons(text, locMap) {
  const lines = text.split(/\r?\n/);
  const weapons = [];
  let cur = null;
  const push = () => { if (cur && cur.id) weapons.push(cur); };
  for (const line of lines) {
    let m;
    if ((m = line.match(/^\s*m_WeaponId:\s*(.+?)\s*$/))) {
      push();
      cur = { id: m[1], damageMin: 0, damageMax: 0, nameLocId: '', sprite: '' };
    } else if (cur && (m = line.match(/^\s*m_DamageMin:\s*(-?\d+)/))) {
      cur.damageMin = +m[1];
    } else if (cur && (m = line.match(/^\s*m_DamageMax:\s*(-?\d+)/))) {
      cur.damageMax = +m[1];
    } else if (cur && (m = line.match(/^\s*m_NameLocalizationId:\s*(.+?)\s*$/))) {
      cur.nameLocId = m[1];
    } else if (cur && (m = line.match(/^\s*m_WeaponSprite:\s*(.+?)\s*$/))) {
      cur.sprite = m[1];
    }
  }
  push();

  // Resolve display names; de-dupe ids (prefab lists each weapon once, but guard).
  const seen = new Set();
  const out = [];
  for (const w of weapons) {
    if (seen.has(w.id)) continue;
    seen.add(w.id);
    const name = locMap.get(w.nameLocId) || prettify(w.id);
    out.push({ id: w.id, name, damageMin: w.damageMin, damageMax: w.damageMax, sprite: w.sprite });
  }
  out.sort((a, b) => a.id.localeCompare(b.id));
  return out;
}

// Fallback readable name from an id when no localization term exists.
function prettify(id) {
  return id.replace(/_/g, ' ').replace(/([a-z])([A-Z])/g, '$1 $2');
}

const locMap = parseLocalization(readFileSync(I2, 'utf8'));
const weapons = parseWeapons(readFileSync(GAME_PARAMS, 'utf8'), locMap);

const body = weapons
  .map((w) => {
    const key = /^[A-Za-z_$][A-Za-z0-9_$]*$/.test(w.id) ? w.id : JSON.stringify(w.id);
    const fields = `name: ${JSON.stringify(w.name)}, damageMin: ${w.damageMin}, damageMax: ${w.damageMax}, sprite: ${JSON.stringify(w.sprite)}`;
    return `  ${key}: { ${fields} },`;
  })
  .join('\n');

const file = `/**
 * Authoritative Fallout Shelter weapon data.
 *
 * GENERATED by scripts/build-weapon-data.mjs from the game's GameParameters.prefab
 * (ids, damage, sprite) and I2Languages.prefab (display names). Do not edit by hand —
 * re-run the generator instead. ${weapons.length} weapons.
 *
 * id: the literal m_WeaponId stored in save files (equipedWeapon.id). Using any other
 * string makes the game replace the weapon with the default .32 pistol on load.
 * sprite: m_WeaponSprite, used to resolve the atlas icon.
 */

// prettier-ignore
export const WEAPON_DATA = {
${body}
};
`;

writeFileSync(OUT, file);
console.log(`Wrote ${OUT} — ${weapons.length} weapons (${weapons.filter((w) => w.name && !w.name.includes(' ') ? 0 : 1).length} named)`);
